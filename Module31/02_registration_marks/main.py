import re

text = 'А578ВЕ777 ОР233787 К901МН666 СТ46599 СНИ2929П777 666АМР666 A545DE777'
taxi = re.findall(r'\b[АВЕКМНОРСТУХ]{2}\d{3}\d{2,3}\b', text)
private_car = re.findall(r'\b[АВЕКМНОРСТУХ]\d{3}[АВЕКМНОРСТУХ]{2}\d{2,3}\b', text)

print('Список номеров частных автомобилей: {}\nСписок номеров такси: {}'.format(
    private_car, taxi
))
